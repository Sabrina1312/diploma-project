/**
 * Модуль управления согласием на использование cookies
 * Реализует механизм "opt-in" - запрашивает разрешение перед сохранением данных
 * Соответствует требованиям GDPR и законодательства о конфиденциальности
 */

(function () {
  // ============================================================
  // КОНСТАНТЫ И НАСТРОЙКИ
  // ============================================================

  /** Ключ для хранения статуса согласия в localStorage */
  const COOKIE_KEY = "cookiesAccepted";

  // ============================================================
  // ПРОВЕРКА СУЩЕСТВУЮЩЕГО СОГЛАСИЯ
  // ============================================================

  /**
   * Если пользователь уже дал согласие ранее:
   * - Баннер не создаётся
   * - Сторонние скрипты (аналитика, реклама) могут загружаться
   * - Функция завершает работу (early return)
   */
  if (localStorage.getItem(COOKIE_KEY) === "true") {
    return; // Выход из IIFE - баннер не нужен
  }

  // ============================================================
  // СОЗДАНИЕ DOM-ЭЛЕМЕНТОВ БАННЕРА
  // ============================================================

  /**
   * HTML-разметка баннера для запроса согласия
   * Содержит:
   * - Информацию о файлах cookie
   * - Ссылку на политику конфиденциальности
   * - Кнопку согласия
   */
  const bannerHTML = `
    <div class="cookie-notice" id="cookieNotice">
      <p>
        Мы используем файлы cookie. Продолжая пользоваться сайтом вы даете
        согласие на использование cookies, а также ознакомлены и даете
        <a class="link link-hover" href="privacy-policy.html">согласие на обработку персональных данных</a>.
      </p>
      <button type="button" class="button cookie-agree" id="cookieAgreeBtn">СОГЛАСИТЬСЯ</button>
    </div>
  `;

  /**
   * Вставка баннера в конец тела документа
   * insertAdjacentHTML с позицией "beforeend" добавляет содержимое
   * как последнего дочернего элемента внутри body
   */
  document.body.insertAdjacentHTML("beforeend", bannerHTML);

  // ============================================================
  // ПОЛУЧЕНИЕ ССЫЛОК НА DOM-ЭЛЕМЕНТЫ
  // ============================================================

  /** Сам баннер (контейнер с уведомлением) */
  const notice = document.getElementById("cookieNotice");

  /** Кнопка согласия на использование cookie */
  const agreeBtn = document.getElementById("cookieAgreeBtn");

  /**
   * Кнопка сброса согласия (опционально)
   * Может использоваться на странице "Настройки конфиденциальности"
   * При клике удаляет сохранённое согласие и перезагружает страницу
   */
  const resetBtn = document.getElementById("resetCookiesBtn");

  // ============================================================
  // ОСНОВНЫЕ ФУНКЦИИ
  // ============================================================

  /**
   * Функция принятия cookies
   * Сохраняет статус согласия и скрывает баннер
   *
   * После вызова этой функции:
   * 1. В localStorage записывается флаг согласия
   * 2. Баннер скрывается (становится невидимым)
   * 3. Могут загружаться аналитические и маркетинговые скрипты
   *    (эту логику нужно добавить отдельно)
   */
  function acceptCookies() {
    // Сохраняем согласие в localStorage (постоянное хранение)
    localStorage.setItem(COOKIE_KEY, "true");

    // Скрываем баннер, добавляя CSS-класс "hidden"
    // Обычно этот класс задаёт display: none
    if (notice) notice.classList.add("hidden");
  }

  /**
   * Функция сброса согласия (для тестирования или настроек)
   * Удаляет сохранённое согласие и перезагружает страницу
   *
   * Полезна для:
   * - Отладочных целей
   * - Страницы настроек конфиденциальности
   * - Кнопки "Отозвать согласие"
   */
  function resetCookies() {
    // Удаляем ключ из localStorage (согласие аннулировано)
    localStorage.removeItem(COOKIE_KEY);

    // Перезагружаем страницу, чтобы баннер появился снова
    // и все скрипты переинициализировались без cookies
    location.reload();
  }

  // ============================================================
  // НАВЕШИВАНИЕ ОБРАБОТЧИКОВ СОБЫТИЙ
  // ============================================================

  /**
   * Обработчик для кнопки согласия
   * При клике сохраняет согласие и скрывает баннер
   */
  if (agreeBtn) agreeBtn.addEventListener("click", acceptCookies);

  /**
   * Обработчик для кнопки сброса (если существует)
   * При клике удаляет согласие и перезагружает страницу
   */
  if (resetBtn) resetBtn.addEventListener("click", resetCookies);

  // ============================================================
  // ПРИМЕЧАНИЯ ПО РАСШИРЕНИЮ ФУНКЦИОНАЛА
  // ============================================================

  /**
   * ДЛЯ ПОЛНОЦЕННОЙ РАБОТЫ С COOKIE НЕОБХОДИМО ДОБАВИТЬ:
   *
   * 1. Загрузку скриптов аналитики после согласия:
   *    - Google Analytics
   *    - Яндекс.Метрику
   *    - Facebook Pixel
   *
   * 2. Более детальные категории согласия:
   *    - Необходимые (всегда активны)
   *    - Аналитические (можно отключить)
   *    - Маркетинговые (можно отключить)
   *
   * 3. Отображение категорий в интерфейсе:
   *    - Чекбоксы для каждой категории
   *    - Кнопка "Сохранить настройки"
   *
   * ПРИМЕР РАСШИРЕННОЙ СТРУКТУРЫ LOCALSTORAGE:
   * localStorage.setItem('cookiePreferences', JSON.stringify({
   *   necessary: true,    // всегда true
   *   analytics: true,    // по выбору пользователя
   *   marketing: false    // по выбору пользователя
   * }));
   */
})();
